#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_ft(int *nbr)
{
	*nbr = 42;
}

int main()
{
	int a;
	int *b;

	b = &a;
	ft_ft(b);
	ft_putchar(*b);
	ft_putchar(a);
	return (0);
}
