int		ft_atoi(char *str)
{
	int i;
	int buff;
	int z;

	i = 0;
	buff = 0;
	while (str[i] < 48 || str[i] > 57 || str[i] == 43 || str[i] == 45)
	{
		i++;
	}
	if (str[(i - 1)] == 45)
	{
		z = 0;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		buff = (buff + (str[i] - 48)) * 10;
		i++;
	}
	if (z == 0)
	{
		buff = (-buff);
	}
	return (buff / 10);
}
