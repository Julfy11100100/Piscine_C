char	*ft_strcpy(char *dest, char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (src[i] != '\0')
	{
		if (dest[i] != '\0')
			j++;
		i++;
	}
	if (j == i)
		i = 0;
	{
		while (i != j)
		{
			dest[i] = src[i];
			i++;
		}
		if (dest[i] != '\0')
			dest[i] = '\0';
		return (dest);
	}
}
