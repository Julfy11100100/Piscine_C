int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (*s1 == *s2 && *s1 != '\0' && i < n)
	{
		s1++;
		s2++;
		i++;
	}
	if (i == n)
		return (0);
	else
	{
		if (*s1 > *s2)
			return (1);
		else
			return (-1);
	}
}
